#include <unistd.h>

void	ptc(char c)
{
	write(1, &c, 1);
}

void	loop(long n)
{
	if (n < 10)
		ptc(n + '0');
	else if (n >= 10)
	{
		loop(n / 10);
		ptc((n % 10) + '0');
	}
}

void	ft_putnbr(int nb)
{
	long n;

	n = nb;
	if (n < 0)
	{
		ptc('-');
		loop(-n);
	}
	else
		loop(n);
}
