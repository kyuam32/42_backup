#include <unistd.h>

void	print(char a, char b, char c)
{
	write(1, &a, 1);
	write(1, &b, 1);
	write(1, &c, 1);
	if (!(a == '7' && b == '8' && c == '9'))
		write(1, &", ", 2);
}

void	ft_print_comb(void)
{
	char a;
	char b;
	char c;

	a = '0';
	b = '1';
	c = '1';
	while (a <= '6' && b <= '8' && c <= '9')
	{
		c++;
		if (c == ':')
		{
			b++;
			if (b == '9')
			{
				a++;
				b = a + 1;
				c = b + 1;
			}
			else
				c = b + 1;
		}
		print(a, b, c);
	}
}
