int		ft_sqrt(int nb)
{
	long long sqr;

	sqr = 1;
	if (nb == 1)
		return (1);
	while (sqr <= nb / sqr)
	{
		if (sqr * sqr == nb)
			return (sqr);
		sqr++;
	}
	return (0);
}
